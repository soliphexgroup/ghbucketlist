export function getExperienceBySlug(slug: string) {
  return experiences.find((e) => e.slug === slug);
}

export function getExperienceById(id: string) {
  return experiences.find((e) => e.id === id);
}

export function getExperiencesByCategory(categoryId: string) {
  return experiences.filter((e) => e.categoryId === categoryId);
}

export function getPriceFrom(exp: Experience) {
  if (exp.isFree) return 0;
  return Math.min(...exp.ticketTypes.map((t) => t.priceGHS));
}
